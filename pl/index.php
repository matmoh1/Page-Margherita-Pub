            <h2>Nasza historia</h2>
            <br/>
            <p class="justify">W 1972 rok znajomi z WAT Mateusz i Dawid otworzyli pierwszą pizzerie w polskim mieście Warszawa, w województwie mazowieckim. Już rok po jej otwarciu zastała oficjalnie najlepszą pizzerią w stolicy polski. Następnie po dokładnie 50 latach powstała ta oto strona, która stała się oknem na świat dla najlepszej pizzy w mieście. W tych trudnych pandemicznych czasach i przymusie rewolucji w strukturze firmy, która z najpopularniejszego lokalu stała się najlepszą internetową pizzerią z tradycjami zapraszamy was do spróbowania pizzy ze słynnego już Margherita Pub.
